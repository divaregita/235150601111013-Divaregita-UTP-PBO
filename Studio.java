public class Studio {
    
    private Movie movie;
    private String type;
    private boolean[] seats;

    public Studio(String type) {
        this.movie = movie;
        this.type = type;
        this.seats = seats;
    }

    public Movie movie() {
        return movie;
    }
    public String type() {
        return type;
    }
    public boolean seats() {
        return seats;
    }
}


